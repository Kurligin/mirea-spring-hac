def reg_detail_text(
    *,
    title: str,
    format_label: str,
    when: str,
    place: str | None,
    status_ru: str,
    short_code: str | None,
    online_url: str | None,
    late_policy_label: str,
) -> str:
    lines = [f"🎫 {title}", format_label]
    if place:
        lines.append(f"📍 {place}")
    lines.append(f"🗓 {when}")
    lines.append(f"Статус: {status_ru}")
    if short_code:
        lines.append(f"Код: {short_code}")
    if online_url:
        lines.append(f"🔗 Ссылка: {online_url}")
    lines += ["", late_policy_label]
    return "\n".join(lines)


