def confirmed_text(title: str, short_code: str, when: str, place: str | None) -> str:
    lines = [
        "✅ Вы записаны!",
        "",
        f"Мероприятие: {title}",
        f"Когда: {when}",
    ]
    if place:
        lines.append(f"Где: {place}")
    lines += ["", f"Код записи: {short_code}", "Покажите код на входе."]
    return "\n".join(lines)


